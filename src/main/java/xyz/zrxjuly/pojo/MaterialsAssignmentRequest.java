package xyz.zrxjuly.pojo;

public class MaterialsAssignmentRequest {
    private String b_name;
    private String childrenAccount;
    private int quantity;

    public String getB_name() {
        return b_name;
    }

    public void setB_name(String b_name) {
        this.b_name = b_name;
    }

    public String getChildrenAccount() {
        return childrenAccount;
    }

    public void setChildrenAccount(String childrenAccount) {
        this.childrenAccount = childrenAccount;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }


}
